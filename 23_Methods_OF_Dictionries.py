marks = {
    "Haris" : 55,
    "Hasher" : 66,
    "Kashif" : 73,
}
# Methods :
# 01 How To Print All the values of Dict
print(marks.values())
# 02 How to print All the keys of Dict
print(marks.keys())
# 03 How to print All the items of Dict
print(marks.items())
# 04 How to update the Dict
marks.update({"Haris": 98})
print(marks)
# 05 How to get the data of a specific key
print(marks.get("Haris"))