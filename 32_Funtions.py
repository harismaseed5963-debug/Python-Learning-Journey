# Functions in Python
# A function is a block of reusable code that performs a specific task.
# Functions help in organizing code, improving readability, and avoiding repetition.
# Funtions have three main parts:
# 1. Function Definition 
# 2. Function Call
# 3. Function Body
# For example:
# Function Definition
def greet(name="Haris"):
# Function Body
    print('Hello ',name)
# Function Call
greet()