# We Use 'For Loops' When we know in advance that how many times want to repeat
for i in range(1,10):
    print(i)
    i += 1
print("Print Even Numbers Between 1 To 10 Through Using For Loop")
for i in range(0, 10,2):
    print(i)
print("Print Odd Numbers Between 1 To 10 Through Using For Loop")
for i in range(1,10,2):
    print(i)
    
