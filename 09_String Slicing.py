# Print a single character from a string using indexing
name = "Haris"
nameshort = name[3]
print(nameshort)

# Print a substring from a string using slicing
shortname = name[0:9]
print("Your Short Name Is : ", shortname)

# There are two types of String Slicing 
# 1. Positive Slicing(Starts From 0)
# 2. Negative Slicing(Starts From -1)